    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblVehicles.getSelectedRow();
        
        if (selectedRowIndex<0){
        
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;        
        }
        
        DefaultTableModel model = (DefaultTableModel)tblVehicles.getModel();
        Vehicle selectedVehicle = (Vehicle) model.getValueAt(selectedRowIndex, 0);
        history.deleteCar(selectedVehicle);
        
        JOptionPane.showMessageDialog(this, "Vehicle deleted.");
        populateTable();
        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");  
        Date date = new Date();  
        history.setLast_Updated(date);
        
    }//GEN-LAST:event_btnDeleteActionPerformed

