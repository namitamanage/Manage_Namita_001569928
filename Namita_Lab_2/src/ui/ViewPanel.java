    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblVehicles.getModel();
        model.setRowCount(0);
        for(Vehicle v: history.getCatalog()){
        Object[] row=new Object[10];
        row[0]=v;
        row[1]=v.getCarMake();
        row[2]=v.getManufacturingYear();
        row[3]=v.getSerialNumber();
        row[4]=v.getModelNumber();
        row[6]=v.getSeatCount();
        row[7]=v.isAvailability();
        row[8]=v.isMaintenanceExpired();
        row[5]=v.getCity();
        
        model.addRow(row);
        }
    }
